# Conditional Statements

min_score = 50
student_score = int(input('What is your total score? '))


if student_score < min_score:
     print('You do not meet the minimun score!')
elif student_score == min_score:
     print('You scored the exact minimum score!')
else:
     print('You exceeded the minimum score!')


