# String Methods

# Capitalize Method
a_string = "python CODING 123!"
print(a_string)

new_string = a_string.capitalize()
print(new_string)

# Lower Method
lower_string = a_string.lower()
print(lower_string)

# Upper Method
upper_string = a_string.upper()
print('Upper Method: ', upper_string)


# IsDigit Method
only_digit = "123"
isdigit_string = only_digit.isdigit()
print('IsDigit Method: ', isdigit_string)

has_digit = "123ds"
isdigit_string = has_digit.isdigit()
print('IsDigit Method: ', isdigit_string)





