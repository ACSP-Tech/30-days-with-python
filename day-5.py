# Arithmetic Operations
# Addition (+)
# Substraction (-)
# Multiplication (*)
# Division (/)


var_1 = 20
var_2 = var_1 + 2  # Addition

print(var_1)
print(var_2)

var_3 = var_1 - 2  # Substraction
print(var_3)

var_4 = var_1 * 2  # Multiplication
print(var_4)

var_5 = var_1 / 2  # Division
print(var_5)

var_6 = var_1 // 2  # Floor Division
print(var_6)

var_7 = var_1 % 2  # Modulus
print(var_7)

var_8 = 20 ** 2  # Power operator
print(var_8)

var_9 = 'Orange' + ' Juice'  # String Arithmetic
print(var_9)


var_10 =  var_9 * 4 # String Arithmetic
print(var_10)






